cities = ['hainan','shanghai','beijing','fujian']
print(cities)

print('Here is the original list:')
print(cities)

print('Here is the sorted list:')
print(sorted(cities))

print('Here is the reverse sorted list:')
print(sorted(cities, reverse=True))

print('Here is the original list:')
print(cities)

cities.reverse()
print(cities)

cities.reverse()
print(cities)

cities.sort()
print(cities)

cities.sort(reverse=True)
print(cities)
